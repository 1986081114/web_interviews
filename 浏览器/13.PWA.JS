/* 
  说说你对spa的理解： 
     spa仅在web页面初始化时加载相应的html， js， css， 一旦页面加载完毕， spa不会因为用户的操作二重新加载或跳转， 取而代之的是利用路由机制实现html内容的变化，
     避免用户重新加载。
     缺点： 初次加载耗时多， 
            前进后退路由管理， 由于单页面只是在一个页面显示内容， 所以不能使用浏览器的后退功能， 所有页面切换需要自己建立栈堆管理
            seo难度大： 由于所有内容都在页面内动态显，seo弱势。
      优点：  用户体验好， 内容改变不用重新加载整个页面， 避免不必要的跳转
             服务器压力小
             前后端职责分离。 架构清晰。
 */

